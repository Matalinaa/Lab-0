# Игнатьева Алёна ПИ 1-1. Упражнение 3 - Площадь комнаты

# Запрашиваем у пользователя длину и ширину комнаты:

print ("Введите длину комнаты (в метрах): ")
length = float(input())

print ("Введите ширину комнаты (в метрах): ")
width = float(input())

# Считаем площадь

S = length * width

# Выводим значение площади на экран

print ("Площадь комнаты =", S, "м кв.")
